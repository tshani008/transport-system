    private void loadEmployeeTable(SetOfEmployees emp) throws IOException, ClassNotFoundException
  {
      data = new Vector<Vector<String>>();
      for(Employee employee:emp)
          {
            Vector v=new Vector();
            v.add(employee.getEmpID());
            v.add(employee.getEmpName());
            v.add(employee.getDesignation());

            
            data.add(v);
           
            
          }
        model = new DefaultTableModel(data,header_Employees);
      
        tblEmployee.setModel(model);
        sorter_Employees = new TableRowSorter<TableModel> (model);
        tblEmployee.setRowSorter(sorter_Employees);
        jScrollPane1.setViewportView(tblEmployee);
        tblEmployee.setAutoscrolls(true);
        tblEmployee.getTableHeader().setReorderingAllowed(false);
        
 }
    
    private void selectEmployeeRow() {
      
        //retrieving the selected row index
        int row = tblEmployee.getSelectedRow();

        //if a single row is selected from the table, get selectedBookNo
        if (tblEmployee.getRowSelectionAllowed()) {
            selectedEmpNo=tblEmployee.getValueAt(row, 0).toString();
            lblEmpID.setText(selectedEmpNo);
            txtname.setText(tblEmployee.getValueAt(row, 1).toString());
            cmbdesignation.setSelectedItem(tblEmployee.getValueAt(row, 2).toString());
           
        }
    }
 

