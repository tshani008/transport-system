    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
         int response = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this account?", "Confirm",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
       if (response == JOptionPane.NO_OPTION) 
       {               
       btndelete.setVisible(false);
       }
       else if (response == JOptionPane.YES_OPTION)
       {
         
        allEmployees.removeEmployee(allEmployees.findEmployeeFromEmpNumber(selectedEmpNo));
             try {
                 loadEmployeeTable(allEmployees);
             } catch (IOException ex) {
                 Logger.getLogger(AddEmpUI1.class.getName()).log(Level.SEVERE, null, ex);
             } catch (ClassNotFoundException ex) {
                 Logger.getLogger(AddEmpUI1.class.getName()).log(Level.SEVERE, null, ex);
             }
        
               
             try {
                 serialize_all();
             } catch (IOException ex) {
                 Logger.getLogger(AddEmpUI1.class.getName()).log(Level.SEVERE, null, ex);
             } catch (ClassNotFoundException ex) {
                 Logger.getLogger(AddEmpUI1.class.getName()).log(Level.SEVERE, null, ex);
             }
               
        JOptionPane.showMessageDialog(rootPane, "Account is been deleted successfully !");
           
       }
    }//GEN-LAST:event_btndeleteActionPerformed

